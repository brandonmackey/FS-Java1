package com.BMackey.java1wk2_2;


public enum myEnum {
	
	Indianapolis("59", "45", "Sunny"),
	Orlando("70", "65", "Cloudy"),
	Denver("46", "32", "Snow");
	
	private final String hi;
	private final String lo;
	private final String dayType;
	
	private myEnum(String hi, String lo, String dayType){
		
		this.hi = hi;
		this.lo = lo;
		this.dayType = dayType;

	}
	
	public String setHi(){
		return hi;
	}
	
	public String setLo(){
		return lo;
	}
	
	public String setDayType(){
		return dayType;
	}
	
}
